st.markdown("""
            Here's a step-by-step guide for beginners to perform dumbbell curls and deadlifts:

### Bicep Curls:""")

st.image('../assets/bicep.png')

st.markdown("""
1. Stand with your feet shoulder-width apart, toes pointing forward.

2. Hold a dumbbell in each hand with palms facing forward.

3. Keeping your upper arms still, bend your elbows to curl the dumbbells up towards your shoulders.

4. Focus on curling with your forearms, not just your biceps.

5. Slowly lower the dumbbells back to the starting position.

6. Repeat for 8-12 reps.

7. Rest for 60-90 seconds between sets.

8. Aim for 3-4 sets.
""")
st.markdown("""
### Squats:
""")

st.image('../assets/prisoner-squat.webp')

st.markdown("""
1- Stand with feet shoulder-width apart, toes pointing straight ahead or slightly outward.

2- Hold dumbbells at sides with palms facing thighs.

3- Keep back straight, engage core, squeeze shoulder blades together.

4- Hinge at hips, bend knees slightly, lower torso while keeping it straight.

5- Lower dumbbells down towards ground, keeping close to legs.

6- Push through heels to return to standing, squeezing glutes at top.

7- Keep movement slow and controlled throughout.

8- Repeat for 8-12 reps.

9- Rest for 60-90 seconds between sets.

10- Aim for 3-4 sets.

### Key Points:

    
- Start light and focus on proper form before increasing weight.
- Breathe naturally, inhaling as you lower and exhaling as you lift.
- Keep your back straight throughout both exercises.
- Focus on controlled movements rather than speed.
- Listen to your body and stop if you experience pain or discomfort.

##### *Remember to consult with a fitness professional or doctor before starting any new exercise routine, especially if you have any health concerns.*
            """)